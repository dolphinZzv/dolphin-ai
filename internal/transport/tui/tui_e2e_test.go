// The permission dialog is modal: pressing ordinary keys (e.g. letters that
// aren't y/a/n) must NOT type into the textarea while the dialog is open.
func TestE2E_PermissionDialogSwallowsOtherKeys(t *testing.T) {
	sim := newSim(80, 24)
	permCh := make(chan string, 1)
	sim.send(permRequestMsg{prompt: "Allow?", ch: permCh})
	sim.stepAll()

	// A random letter neither resolves the dialog nor reaches the textarea.
	sim.send(tea.KeyPressMsg{Code: 'x'})
	sim.stepAll()

	if sim.m.permDialog == nil {
		t.Error("dialog should still be open after unrelated key")
	}
	if sim.m.textarea.Value() != "" {
		t.Errorf("textarea should be empty (modal swallows keys), got %q", sim.m.textarea.Value())
	}

	// Arrow-key navigation moves the active choice without resolving.
	sim.send(tea.KeyPressMsg{Code: tea.KeyRight})
	sim.stepAll()
	if sim.m.permDialog == nil {
		t.Fatal("dialog should still be open after right arrow")
	}
	if sim.m.permDialog.active != 1 {
		t.Errorf("right arrow should move active to index 1, got %d", sim.m.permDialog.active)
	}

	// Enter confirms the highlighted choice (index 1 → always).
	sim.send(tea.KeyPressMsg{Code: tea.KeyEnter})
	sim.stepAll()
	if sim.m.permDialog != nil {
		t.Error("dialog should be dismissed after enter")
	}
	select {
	case choice := <-permCh:
		if choice != "always" {
			t.Errorf("expected 'always' from enter on index 1, got %q", choice)
		}
	default:
		t.Error("expected permission choice to be received")
	}
}

func TestE2E_LiveRenderDuringStreamingMerge(t *testing.T) {
	sim := newSim(100, 30)

	// Start streaming text with markdown markers.
	sim.send(contentMsg{text: "First **bold"})
	sim.stepAll()
	// Even before the closing **, the viewport shows *something* rendered —
	// never empty once text has arrived.
	if sim.m.renderedContent == "" {
		t.Error("rendered content should be non-empty during streaming")
	}

	// Streaming a merge that completes the bold marker must re-render the
	// whole text block live; the literal ** must be consumed by glamour.
	sim.send(contentMsg{text: "** chunk"})
	sim.stepAll()
	if strings.Contains(sim.m.renderedContent, "**bold**") {
		t.Error("merged text block should be markdown-rendered live, not raw")
	}

	// A tool call arriving mid-stream must not regress the already-rendered
	// text block back to raw source.
	sim.send(toolCallMsg{call: types.ToolCall{Name: "finalize_test", Arguments: "{}"}})
	sim.stepAll()
	if strings.Contains(sim.m.renderedContent, "**bold**") {
		t.Error("non-text entry must not undo live markdown rendering")
	}
}

func TestE2E_LiveRenderFinalizedByFlush(t *testing.T) {
	sim := newSim(100, 30)

	// Stream a complete markdown construct across two deltas.
	sim.send(contentMsg{text: "First `code"})
	sim.stepAll()
	sim.send(contentMsg{text: "` chunk"})
	sim.stepAll()
	if strings.Contains(sim.m.renderedContent, "`code`") {
		t.Error("inline code should be markdown-rendered live during streaming")
	}

	// Flush must leave the content rendered.
	sim.send(flushMsg{})
	sim.stepAll()
	if strings.Contains(sim.m.renderedContent, "`code`") {
		t.Error("rendered content should not contain raw markdown markers after flush")
	}
}
